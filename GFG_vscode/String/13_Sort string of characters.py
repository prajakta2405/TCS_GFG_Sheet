'''
Given a string consisting of lowercase letters, arrange all its letters in ascending order. 

Example 1:

Input:
S = "edcab"
Output: "abcde"
Explanation: characters are in ascending
order in "abcde".
'''
#User function Template for python3
class Solution:
    def sort(self, s): 
        #code here
        s=''.join(sorted(s))
        return s


