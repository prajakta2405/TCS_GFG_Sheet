'''
Given a string, remove spaces from it. 

Example 1:

Input:
S = "geeks  for geeks"
Output: geeksforgeeks
Explanation: All the spaces have been 
removed.
'''
#User function Template for python3

class Solution:
    def modify(self, s):
        # code here
        res=""
        for i in s:
            if i!=" ":
                res+=i
        return res

